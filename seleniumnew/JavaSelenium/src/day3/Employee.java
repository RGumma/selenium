package day3;

public class Employee {
	
	private String name;
	private String jobTitle;
	private int numberOfDeapartment;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getJobTitle() {
		return jobTitle;
	}

	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}

	public int getNumberOfDeapartment() {
		return numberOfDeapartment;
	}

	public void setNumberOfDeapartment(int numberOfDeapartment) {
		this.numberOfDeapartment = numberOfDeapartment;
	}
	

}
